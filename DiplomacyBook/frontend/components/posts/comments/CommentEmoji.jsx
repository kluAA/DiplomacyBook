class CommentEmoji extends React.Component {
    constructor(props) {
        super(props);
        this.state = {
            showMenu: false
        }
        this.handleClick = this.handleClick.bind(this);
        this.smiley = React.createRef();
        this.people = React.createRef();
        this.clothes = React.createRef();
        this.animals = React.createRef();
        this.foods = React.createRef();
    }

    componentDidMount() {
        document.addEventListener('mousedown', this.handleClick, false);
    }

    componentWillUnmount() {
        document.removeEventListener('mousedown', this.handleClick, false);
    }


    handleClick(e) {
        const domNode = ReactDOM.findDOMNode(this);

        if (!domNode || !domNode.contains(e.target)) {
            this.setState({
                showMenu: false
            });
        }
    }

    scroll(ref) {
        document.getElementById("emojis-list").scrollTo({behavior: "smooth", top: ref.current.offsetTop - 8 })
    }

    render() {
        const { addEmoji } = this.props;
        const menu = (
            <div className="emoji-menu">
                <div className="emojis-list" id="emojis-list">
                    <div className="emojis-title" ref={this.smiley}>Smileys</div>
                    {smileys.map((smiley, i) => <li onClick={e => addEmoji(smiley)} key={"s-"+i}>{smiley}</li>)}
                    <div className="emojis-title" ref={this.people}>People</div>
                    {people.map((person, i) => <li onClick={e => addEmoji(person)} key={"p-"+i}>{person}</li>)}
                    <div className="emojis-title" ref={this.clothes}>Clothes</div>
                    {clothes.map((cloth, i) => <li onClick={e => addEmoji(cloth)} key={"c-"+i}>{cloth}</li>)}
                    <div className="emojis-title" ref={this.animals}>Animals {'&'} Nature</div>
                    {animals.map((animal, i) => <li onClick={e => addEmoji(animal)} key={"a-"+i}>{animal}</li>)}
                    <div className="emojis-title" ref={this.foods}>Food {'&'} Drink</div>
                    {foods.map((food, i) => <li onClick={e => addEmoji(food)} key={"f-"+i}>{food}</li>)}
                </div>
                <ul className="emojis-nav">
                    <li>
                        <i className="far fa-smile" onClick={e => this.scroll(this.smiley)}></i>
                    </li>
                    <li>
                        <i className="far fa-thumbs-up" onClick={e => this.scroll(this.people)}></i>
                    </li>
                    <li>
                        <i className="fas fa-tshirt" onClick={e => this.scroll(this.clothes)}></i>
                    </li>
                    <li>
                        <i className="fas fa-paw" onClick={e => this.scroll(this.animals)}></i>
                    </li>
                    <li>
                        <i className="fas fa-hamburger" onClick={e => this.scroll(this.foods)}></i>
                    </li>
                </ul>
                <div className="outer-triangle">
                    <div className="inner-triangle"></div>
                </div>
            </div>
        )

        return (
            <div id="emoji-container">
                <i className="far fa-smile-wink" onClick={e => this.setState({ showMenu: !this.state.showMenu })}>
                </i>
                    { this.state.showMenu && menu }
            </div>
        )
    }
}

export default CommentEmoji;